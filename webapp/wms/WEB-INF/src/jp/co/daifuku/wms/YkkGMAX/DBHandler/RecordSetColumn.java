package jp.co.daifuku.wms.YkkGMAX.DBHandler;

public class RecordSetColumn 
{

	private String name;
	private String value;

	public RecordSetColumn(String name, String value)
	{
		this.name = name;
		this.value = value;
	}
	
	public String getName() 
	{
		return name;		
	}

	public String getValue() 
	{
		return value;
	}

}
