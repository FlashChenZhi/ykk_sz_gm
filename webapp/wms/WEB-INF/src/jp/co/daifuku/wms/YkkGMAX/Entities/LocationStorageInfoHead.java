package jp.co.daifuku.wms.YkkGMAX.Entities;

import java.util.ArrayList;

public class LocationStorageInfoHead
{
	private String depo = "";
	
	private ArrayList locationStatus = new ArrayList();
	
	private ArrayList weightReportFlag = new ArrayList();
	
	private String locationNoFrom = "";
	
	private String locationNoTo = "";
	
	private boolean rangeSet = false;

	public boolean isRangeSet()
	{
		return rangeSet;
	}

	public void setRangeSet(boolean rangeSet)
	{
		this.rangeSet = rangeSet;
	}

	public String getLocationNoFrom()
	{
		return locationNoFrom;
	}

	public void setLocationNoFrom(String locationNoFrom)
	{
		this.locationNoFrom = locationNoFrom;
	}

	public String getLocationNoTo()
	{
		return locationNoTo;
	}

	public void setLocationNoTo(String locationNoTo)
	{
		this.locationNoTo = locationNoTo;
	}

	public ArrayList getLocationStatus()
	{
		return locationStatus;
	}

	public void setLocationStatus(ArrayList locationStatus)
	{
		this.locationStatus = locationStatus;
	}

	public String getDepo()
	{
		return depo;
	}

	public void setDepo(String depo)
	{
		this.depo = depo;
	}

	public ArrayList getWeightReportFlag()
	{
		return weightReportFlag;
	}

	public void setWeightReportFlag(ArrayList weightReportFlag)
	{
		this.weightReportFlag = weightReportFlag;
	}

}
