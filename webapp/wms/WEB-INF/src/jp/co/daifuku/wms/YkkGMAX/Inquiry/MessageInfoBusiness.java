	private void setHeadSession()
	{
		MessageInfoHead head = new MessageInfoHead();

		head.setDateFrom(StringUtils.formatDateFromPageToDB(txt_DateFrom
				.getText()));
		if (!StringUtils.IsNullOrEmpty(txt_TimeFrom.getText()))
		{
			head.setTimeFrom(StringUtils.formatTimeFormPageToDB(txt_TimeFrom
					.getText()));
		}
		else
		{
			head.setTimeFrom("000000");
		}
		head
				.setDateTo(StringUtils.formatDateFromPageToDB(txt_DateTo
						.getText()));
		if (!StringUtils.IsNullOrEmpty(txt_TimeTo.getText()))
		{
			head.setTimeTo(StringUtils.formatTimeFormPageToDB(txt_TimeTo
					.getText()));
		}
		else
		{
			head.setTimeTo("000000");
		}
		head.setMessageType(pul_MessageDivision.getSelectedValue());

		session.setAttribute(MESSAGE_INFO_HEAD, head);

	}

