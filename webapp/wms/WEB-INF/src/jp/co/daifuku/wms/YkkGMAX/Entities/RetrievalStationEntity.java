package jp.co.daifuku.wms.YkkGMAX.Entities;

public class RetrievalStationEntity
{
	private String retrievalStation = "";

	private String retrievalStationName = "";

	private String pickStation = "";

	private String unitStation = "";

	public String getRetrievalStation()
	{
		return retrievalStation;
	}

	public void setRetrievalStation(String retrievalStation)
	{
		this.retrievalStation = retrievalStation;
	}

	public String getRetrievalStationName()
	{
		return retrievalStationName;
	}

	public void setRetrievalStationName(String retrievalStationName)
	{
		this.retrievalStationName = retrievalStationName;
	}

	public String getPickStation()
	{
		return pickStation;
	}

	public void setPickStation(String pickStation)
	{
		this.pickStation = pickStation;
	}

	public String getUnitStation()
	{
		return unitStation;
	}

	public void setUnitStation(String unitStation)
	{
		this.unitStation = unitStation;
	}
}
